package com.example.recyclecard;

public class example_item {

    private int mImageResourse;
    private String mText;

    public example_item(int imageResourse, String text){
        mImageResourse = imageResourse;
        mText = text;
    }

    public int getmImageResourse() {
        return mImageResourse;
    }

    public String getmText() {
        return mText;
    }
}

